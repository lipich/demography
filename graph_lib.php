<?php
// получить цвет для графика
function GetColor($i)
{
    // массив цветов (требует оптимизации и алгоритма)
    $rColor = array(0,0,255, 0,255,255, 0,0,128, 0,128,128, 0,0,64, 0,64,64, 0,0,192, 0,192,192, 0,0,96, 0,96,96, 0,0,48, 0,48,48, 0,0,144, 0,144,144, 0,0,72, 0,72,72, 0,0,216, 0,216,216, 0,0,108, 0,108,108, 0,0,54, 0,54,54, 0,0,162, 0,162,162);
    $gColor = array(0,255,0, 255,0,255, 0,128,0, 128,0,128, 0,64,0, 64,0,64, 0,192,0, 192,0,192, 0,96,0, 96,0,96, 0,48,0, 48,0,48, 0,144,0, 144,0,144, 0,72,0, 72,0,72, 0,216,0, 216,0,216, 0,108,0, 108,0,108, 0,54,0, 54,0,54, 0,162,0, 162,0,162);
    $bColor = array(255,0,0, 255,255,0, 128,0,0, 128,128,0, 64,0,0, 64,64,0, 192,0,0, 192,192,0, 96,0,0, 96,96,0, 48,0,0, 48,48,0, 144,0,0, 144,144,0, 72,0,0, 72,72,0, 216,0,0, 216,216,0, 108,0,0, 108,108,0, 54,0,0, 54,54,0, 162,0,0, 162,162,0);

    $rgb = array();

    if ($i < count($rColor))
    {
        $rgb["r"] = $rColor[$i];
        $rgb["g"] = $gColor[$i];
        $rgb["b"] = $bColor[$i];
    }
    else
    {
        $rgb["r"] = 0;
        $rgb["g"] = 0;
        $rgb["b"] = 0;
    }

    return $rgb;
}
?>